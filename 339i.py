# Description
# A carriage company is renting cars and there is a particular car for which the interest 
# is the highest so the company decides to book the requests one year in advance.
# We represent a request with a tuple (x, y) where x is the first day of the renting and y is the last. 
# Your goal is to come up with an optimum strategy where you serve the most number of requests.
# Input Description
# The first line of the input will be n the number of requests. 
# The following two lines will consist of 
# n numbers for the starting day of the renting,
# followed by another n numbers for the last day of the renting corresponding. 
# For all lines 0 < x i < y i <= 365 inequality holds, where i=1, 2, ..., n.